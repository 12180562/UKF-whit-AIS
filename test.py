def latlon_to_utm(latitude, longitude, zone_number):
    utm_zone = pyproj.Proj(proj='utm', zone=zone_number, datum='WGS84')
    utm_easting, utm_northing = utm_zone(longitude, latitude)

    return utm_easting, utm_northing


